package com.nursultanturdaliev.moneytransferapp.response;

public class Rates {
    private Double usd;
    private  Double euro;
    private Double ruble;
    private Double tenge;


    public Double getUsd() {
        return usd;
    }

    public void setUsd(Double usd) {
        this.usd = usd;
    }

    public Double getEuro() {
        return euro;
    }

    public void setEuro(Double euro) {
        this.euro = euro;
    }

    public Double getRuble() {
        return ruble;
    }

    public void setRuble(Double ruble) {
        this.ruble = ruble;
    }

    public Double getTenge() {
        return tenge;
    }

    public void setTenge(Double tenge) {
        this.tenge = tenge;
    }
}
